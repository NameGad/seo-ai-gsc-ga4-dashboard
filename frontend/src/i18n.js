export function setLocale(value) {
  if (!['en', 'zh'].includes(value)) return;
  locale.value = value;
  localStorage.setItem('seo-dashboard:locale', value);
  document.documentElement.lang = value === 'zh' ? 'zh-CN' : 'en';
}

export function t(key, fallback = key, vars = {}) {
  const template = messages[locale.value]?.[key] ?? messages.en[key] ?? fallback;
  return interpolate(template, vars);
}

export function tv(value) {
  const key = String(value ?? '');
  return valueMessages[locale.value]?.[key] ?? valueMessages.en[key] ?? key;
}

function interpolate(template, vars) {
  return String(template).replace(/\{(\w+)\}/g, (_, key) => vars[key] ?? '');
}

export function useI18n() {
  return {
    locale,
    languageOptions,
    setLocale,
    t,
    tv
  };
}
